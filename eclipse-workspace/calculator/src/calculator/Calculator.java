package calculator;

public class Calculator {
	void addition( int add1,int add2) {
		int sum=add1+add2;
		System.out.println(sum);	}
	void subtraction(int sub1,int sub2) {
		int difference=sub1-sub2;
		System.out.println(difference);
	}
	void multiplication(int mult1,int mult2) {
		int product=mult1*mult2;
		System.out.println(product);
	}
	void division (int div1,int div2) {
		int result=div1/div2;
		System.out.println(result);
	}

}
